package com.example.advanced_retrofit_using_real_api.POST_RequestCode.POST_Method_with_FormUrlEncode;

public class CreateUserClass {
    private int id;
    private String title;
    private String body;
    private int userId;

    public CreateUserClass() {}

    public CreateUserClass(int id, String title, String body, int userId) {
        this.id = id;
        this.title = title;
        this.body = body;
        this.userId = userId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }
}
